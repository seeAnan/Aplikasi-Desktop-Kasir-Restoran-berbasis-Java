    private void inputTransaksiButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputTransaksiButtonActionPerformed
        // TODO add your handling code here:
        try {
            menu_transaksi tran = new menu_transaksi();
            totalBayar_textField.setText(""+tran.total_bayar);
            this.stat = k.getCon().prepareStatement("insert into transaksi values(?,?,?,?,?,?,?,?)");
            this.stat.setInt(1, 0);
            this.stat.setString(2, tran.nama_pelanggan);
            this.stat.setInt(3, tran.id_menu);
            this.stat.setString(4, tran.tanggal);
            this.stat.setString(5, tran.nama_menu);
            this.stat.setInt(6, tran.harga);
            this.stat.setInt(7, tran.jumlah_order);
            this.stat.setInt(8, tran.total_bayar);
            int pilihan = JOptionPane.showConfirmDialog(null,
                    "Tanggal: "+tran.tanggal+
                    "\nNama Pelanggan: "+tran.nama_pelanggan+
                    "\nPembelian : "+tran.jumlah_order+" "+tran.nama_menu+
                    "\nTotal Bayar: "+tran.total_bayar+"\n",
                    "Tambahkan Transaksi?",
                    JOptionPane.YES_NO_OPTION);
            if (pilihan == JOptionPane.YES_OPTION) {
                    this.stat.executeUpdate();
                    refreshTable();
            } else if(pilihan == JOptionPane.NO_OPTION){
                refreshTable();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_inputTransaksiButtonActionPerformed

    private void uploadTransaksiButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadTransaksiButtonActionPerformed
        // TODO add your handling code here:
        try {
            menu_transaksi tran = new menu_transaksi();
            tran.id_transaksi = Integer.parseInt(idTransaksi_textField.getText());
            this.stat = k.getCon().prepareStatement("update transaksi set nama_pelanggan=?,"
                    + "id_menu=?,tanggal=?,nama_menu=?,harga=?,jumlah_order=?,total_bayar=? "
                    + "where id_transaksi=?");
            this.stat.setString(1, tran.nama_pelanggan);
            this.stat.setInt(2, tran.id_menu);
            this.stat.setString(3, tran.tanggal);
            this.stat.setString(4, tran.nama_menu);
            this.stat.setInt(5, tran.harga);
            this.stat.setInt(6, tran.jumlah_order);
            this.stat.setInt(7, tran.total_bayar);
            this.stat.setInt(8, tran.id_transaksi);
            this.stat.executeUpdate();
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_uploadTransaksiButtonActionPerformed

