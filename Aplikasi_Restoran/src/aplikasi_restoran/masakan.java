    private void inputMenuButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputMenuButtonActionPerformed
        // TODO add your handling code here:
        try {
            menu m = new menu();
            this.stat = k.getCon().prepareStatement("insert into menu values(?,?,?,?)");
            stat.setInt(1, 0);
            stat.setString(2, m.nama_menu);
            stat.setInt(3, m.harga);
            stat.setString(4, m.status);
            stat.executeUpdate();
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_inputMenuButtonActionPerformed

