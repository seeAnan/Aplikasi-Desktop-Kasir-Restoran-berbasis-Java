    private void updateMenuButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateMenuButtonActionPerformed
        // TODO add your handling code here:
        try {
            menu m = new menu();
            
            this.stat = k.getCon().prepareStatement("update menu set nama_menu=?,"
                    + "harga=?,status=? where id_menu=?");
            stat.setString(1, m.nama_menu);
            stat.setInt(2, m.harga);
            stat.setString(3, m.status);
            stat.setInt(4, Integer.parseInt(idMenu_textField.getText()));
            stat.executeUpdate();
            refreshTable();
                    
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_updateMenuButtonActionPerformed

    private void deleteMenuButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteMenuButtonActionPerformed
        // TODO add your handling code here:
        try {
            this.stat = k.getCon().prepareStatement("delete from menu where id_menu=?");
            stat.setInt(1, Integer.parseInt(idMenu_textField.getText()));
            stat.executeUpdate();
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_deleteMenuButtonActionPerformed

