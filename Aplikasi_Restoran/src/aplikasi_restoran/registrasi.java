    class user extends registrasi{
        int id_user, id_level;
        String username, password, nama_user;
        
        public user(){
            username = usernameRegis_textField.getText();
            password = passwordRegis_textField.getText();
            nama_user = namaUserRegis_textField.getText();
            id_level = Integer.parseInt(idLevel_comboBox.getSelectedItem().toString());
        }
    }
    
    public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("ID User");
        model.addColumn("Username");
        model.addColumn("Password");
        model.addColumn("Nama User");
        model.addColumn("ID Level");
        tableRegis.setModel(model);
        try {
            this.stat = k.getCon().prepareStatement("select * from user");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {                
                Object[] data = {
                rs.getString("id_user"),
                rs.getString("username"),
                rs.getString("password"),
                rs.getString("nama_user"),
                rs.getString("id_level")
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        idUser_textField.setText("");
        usernameRegis_textField.setText("");
        passwordRegis_textField.setText("");
        namaUserRegis_textField.setText("");
    }
    
