    class menu_transaksi extends transaksi{
        int id_transaksi, id_menu, harga, jumlah_order, total_bayar;
        String nama_pelanggan, tanggal, nama_menu;

        public menu_transaksi() {
            this.nama_pelanggan = namaPelangganTransaksi_textField.getText();
            String combo = idMenu_comboBox.getSelectedItem().toString();
            String[] arr = combo.split(":");
            this.id_menu = Integer.parseInt(arr[0]);
            try {
                Date date = tanggalTextField.getDate();
                DateFormat dateFormat = new SimpleDateFormat("YYYY-MM-dd");
                this.tanggal = dateFormat.format(date);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
            this.nama_menu = arr[1];
            this.harga = Integer.parseInt(arr[2]);
            this.jumlah_order = Integer.parseInt(jmlOrder_textField.getText());
            this.total_bayar = this.harga * this.jumlah_order;
            
        }
        
        
    }
    
    public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("ID Transaksi");
        model.addColumn("Nama Pelanggan");
        model.addColumn("ID Masakan");
        model.addColumn("Tanggal");
        model.addColumn("Nama Masakan");
        model.addColumn("Harga");
        model.addColumn("Jumlah Order");
        model.addColumn("Total Bayar");
        tableTransaksi.setModel(model);
        try {
            this.stat = k.getCon().prepareStatement("select * from transaksi");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {                
                Object[] data = {
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6),
                    rs.getString(7),
                    rs.getString(8)
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        idTransaksi_textField.setText("");
        namaPelangganTransaksi_textField.setText("");
        jmlOrder_textField.setText("");
        totalBayar_textField.setText("");
    }
    
    public void refreshCombo(){
        try {
            this.stat = k.getCon().prepareStatement("select * from menu "
                    + "where status = 'AVAILABLE'");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {                
                idMenu_comboBox.addItem(rs.getString("id_menu")+":"
                +rs.getString("nama_menu")+":"+rs.getString("harga"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

